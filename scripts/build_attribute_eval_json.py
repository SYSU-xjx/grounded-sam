DEFAULT_ATTR_ZH = {
    "size": {
        "largest": "最大",
        "smallest": "最小",
    },
    "spatial": {
        "leftmost": "最左侧",
        "rightmost": "最右侧",
        "topmost": "最上方",
        "bottommost": "最下方",
    },
}


def parse_args():
    parser = argparse.ArgumentParser(
        description="Build attribute-evaluation JSON from COCO manifest and annotations."
    )
    parser.add_argument("--ann_file", type=str, required=True, help="COCO instances annotation file.")
    parser.add_argument("--manifest_file", type=str, required=True, help="Manifest generated by download script.")
    parser.add_argument("--output_file", type=str, required=True, help="Output JSON path.")
    parser.add_argument(
        "--category_zh_map",
        type=str,
        default="",
        help="Optional JSON file: {\"chair\": \"椅子\", ...}",
    )
    parser.add_argument(
        "--color_labels_file",
        type=str,
        default="",
        help=(
            "Optional JSON file for color tasks by ann id: "
            "{\"12345\": \"红色\", \"67890\": \"蓝色\"}"
        ),
    )
    parser.add_argument(
        "--min_instances_for_relative",
        type=int,
        default=2,
        help="Minimum instances per category in one image for size/spatial relative tests.",
    )
    return parser.parse_args()


def load_json(path):
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def bbox_xywh_to_xyxy(b):
    x, y, w, h = b
    return [x, y, x + w, y + h]


def bbox_center(b):
    x, y, w, h = b
    return [x + w / 2.0, y + h / 2.0]


def bbox_area(b):
    return b[2] * b[3]


def add_task(task_list, image_info, cat_en, cat_zh, attribute_type, attribute_value, ann):
    task_list.append(
        {
            "task_id": f"{image_info['id']}_{ann['id']}_{attribute_type}_{attribute_value}",
            "image_id": image_info["id"],
            "file_name": image_info["file_name"],
            "width": image_info["width"],
            "height": image_info["height"],
            "category_en": cat_en,
            "category_zh": cat_zh,
            "attribute_type": attribute_type,
            "attribute_value": attribute_value,
            "attribute_value_zh": DEFAULT_ATTR_ZH.get(attribute_type, {}).get(attribute_value, attribute_value),
            "prompt_zh": f"{DEFAULT_ATTR_ZH.get(attribute_type, {}).get(attribute_value, attribute_value)}的{cat_zh}",
            "prompt_en_ref": f"{attribute_value} {cat_en}",
            "target_ann_id": ann["id"],
            "target_bbox_xywh": ann["bbox"],
            "target_bbox_xyxy": bbox_xywh_to_xyxy(ann["bbox"]),
            "source": "auto_bbox",
        }
    )


def main():
    args = parse_args()
    coco = COCO(args.ann_file)
    manifest = load_json(args.manifest_file)

    cat_zh_map = dict(DEFAULT_CAT_ZH)
    if args.category_zh_map:
        cat_zh_map.update(load_json(args.category_zh_map))

    color_labels = {}
    if args.color_labels_file:
        color_labels = load_json(args.color_labels_file)

    selected_img_ids = sorted(
        {
            img_id
            for _, img_ids in manifest.get("selected_img_ids", {}).items()
            for img_id in img_ids
        }
    )

    selected_cat_names = manifest.get("categories", [])
    selected_cat_ids = coco.getCatIds(catNms=selected_cat_names)
    cat_info = {c["id"]: c["name"] for c in coco.loadCats(selected_cat_ids)}

    out = {
        "meta": {
            "schema_version": "v1.0",
            "ann_file": args.ann_file,
            "manifest_file": args.manifest_file,
            "selected_categories_en": selected_cat_names,
            "min_instances_for_relative": args.min_instances_for_relative,
            "notes": [
                "Size/spatial tasks are auto-generated from bbox statistics.",
                "Color tasks are optional and require color_labels_file for reliable labels.",
                "Prompts are grouped per image for one-shot multi-prompt inference.",
            ],
        },
        "images": [],
    }

    for img_id in selected_img_ids:
        image_info = coco.loadImgs([img_id])[0]
        ann_ids = coco.getAnnIds(imgIds=[img_id], catIds=selected_cat_ids, iscrowd=False)
        anns = coco.loadAnns(ann_ids)
        anns_by_cat = defaultdict(list)
        for ann in anns:
            anns_by_cat[ann["category_id"]].append(ann)

        tasks = []
        for cat_id, cat_anns in anns_by_cat.items():
            cat_en = cat_info[cat_id]
            cat_zh = cat_zh_map.get(cat_en, cat_en)
            if len(cat_anns) < args.min_instances_for_relative:
                continue

            # size
            largest = max(cat_anns, key=lambda a: bbox_area(a["bbox"]))
            smallest = min(cat_anns, key=lambda a: bbox_area(a["bbox"]))
            add_task(tasks, image_info, cat_en, cat_zh, "size", "largest", largest)
            add_task(tasks, image_info, cat_en, cat_zh, "size", "smallest", smallest)

            # spatial
            leftmost = min(cat_anns, key=lambda a: bbox_center(a["bbox"])[0])
            rightmost = max(cat_anns, key=lambda a: bbox_center(a["bbox"])[0])
            topmost = min(cat_anns, key=lambda a: bbox_center(a["bbox"])[1])
            bottommost = max(cat_anns, key=lambda a: bbox_center(a["bbox"])[1])
            add_task(tasks, image_info, cat_en, cat_zh, "spatial", "leftmost", leftmost)
            add_task(tasks, image_info, cat_en, cat_zh, "spatial", "rightmost", rightmost)
            add_task(tasks, image_info, cat_en, cat_zh, "spatial", "topmost", topmost)
            add_task(tasks, image_info, cat_en, cat_zh, "spatial", "bottommost", bottommost)

            # optional color tasks (manual / external labels by ann id)
            for ann in cat_anns:
                ann_id_str = str(ann["id"])
                if ann_id_str in color_labels:
                    color_zh = color_labels[ann_id_str]
                    tasks.append(
                        {
                            "task_id": f"{image_info['id']}_{ann['id']}_color_{color_zh}",
                            "image_id": image_info["id"],
                            "file_name": image_info["file_name"],
                            "width": image_info["width"],
                            "height": image_info["height"],
                            "category_en": cat_en,
                            "category_zh": cat_zh,
                            "attribute_type": "color",
                            "attribute_value": color_zh,
                            "attribute_value_zh": color_zh,
                            "prompt_zh": f"{color_zh}的{cat_zh}",
                            "prompt_en_ref": f"{color_zh} {cat_en}",
                            "target_ann_id": ann["id"],
                            "target_bbox_xywh": ann["bbox"],
                            "target_bbox_xyxy": bbox_xywh_to_xyxy(ann["bbox"]),
                            "source": "manual_color_label",
                        }
                    )

        grouped_prompts_zh = []
        for t in tasks:
            if t["prompt_zh"] not in grouped_prompts_zh:
                grouped_prompts_zh.append(t["prompt_zh"])

        out["images"].append(
            {
                "image_id": image_info["id"],
                "file_name": image_info["file_name"],
                "width": image_info["width"],
                "height": image_info["height"],
                "num_selected_instances": len(anns),
                "prompt_group_zh_list": grouped_prompts_zh,
                "prompt_group_zh_input": "。".join(grouped_prompts_zh),
                "tasks": tasks,
            }
        )

    os.makedirs(os.path.dirname(args.output_file) or ".", exist_ok=True)
    with open(args.output_file, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)

    total_tasks = sum(len(x["tasks"]) for x in out["images"])
    print(f"Done: {args.output_file}")
    print(f"Images: {len(out['images'])}, Tasks: {total_tasks}")


if __name__ == "__main__":
    main()
